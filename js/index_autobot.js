	function getArrayStringBotMsg(bmsg){
		check_bmsg = /\*/.test(bmsg["text"]);
		var bmessage = [];
		if (check_bmsg === false){
			bmessage[0] = bmsg["text"];
		}
		else{
			bmess = bmsg["text"].split("\n\n");
			bmessage = bmess.map(function(s){ return s.slice(2)});
			if(bmessage[0] == "confirm_yes"){
				bmessage[0] = "Yes";
			}
			if(bmessage[1] == "confirm_no"){
				bmessage[1] = "No";
			}
		}
		return bmessage;
	}
	
